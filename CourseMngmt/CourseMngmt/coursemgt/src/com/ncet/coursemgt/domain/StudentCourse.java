package com.ncet.coursemgt.domain;

public class StudentCourse {
	private int stuCourId;
	private int stuId;
	private int courId;
	
	public int getStuCourId() {
		return stuCourId;
	}
	public void setStuCourId(int stuCourId) {
		this.stuCourId = stuCourId;
	}
	public int getStuId() {
		return stuId;
	}
	public void setStuId(int stuId) {
		this.stuId = stuId;
	}
	public int getCourId() {
		return courId;
	}
	public void setCourId(int courId) {
		this.courId = courId;
	}
}
